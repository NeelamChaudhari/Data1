package OOPs_Concept;

public class PrimeNo
{
	public static void main(String[] args)
	{
		int org=7;
		int count=0;
		for(int i=2;i<org;i++)
		{
			if(org % i==0)
			{
				count++;
				break;
				
			
		     }
	
		}
		if(count==1)
				{
					System.out.println("Not");
				}
				else
				{
					System.out.println("Prime");
				}
			}}
	
		
	


